# Comparison Operators:
# 1. Equal To(==)
# 2. Greater than (>)
# 3. Lesser than (<)
# 4. Greater than or Equal to (>=)
# 5. Lesser than or Equal to(<=)
# 6. Not Equal To(!=)


# 1. Equal To(==) - checks if both values are same
var1 = 10
var2 = 10

print(var1 == var2)
# True

a = 10
b = 20
print(a == b)
# False

# 2. Greater than (>)
# Returns True if x is greater than y, else returns False
x = 10
y = 20

print(x>y)
# False

print(y>x)
# True

# Directly
print(-10 > -20)
# True


# Returns True if l is lesser than m, else returns False
# Lesser than (<)
l = 40
m = 5
print(l<m)
# False

print(m<l)
# True

print(-20 < -5)
# True


