# Greater than or Equal to(>=)
# Returns True if v1 is greater than or Equal to v2, else returns False

v1 = 50
v2 = 10
print(v1>=v2)
# True

a = 30
b = 30
print(a>=b)
# True

x = 40
y = 60
print(x>=y)
# False

print(-10>=-20)
# True


# Lesser than or Equal to
# Returns True if a is lesser than or equal to b, else returns False

a = 10
b = 20
print(a<=b)
# True

print(b<=a)
# False

print(-20<=-10)
# True