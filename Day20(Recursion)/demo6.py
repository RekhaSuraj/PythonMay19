# How to define a class
class Person:
    Name = 'ABC'
    Gender = 'Female'
    Profession = 'IT'


# How to create an object/instance of the above class and call its variables
objPerson = Person()

print(objPerson.Name)
print(objPerson.Gender)
print(objPerson.Profession)

# ABC
# Female
# IT


