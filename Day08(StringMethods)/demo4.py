# startswith
str1 = 'Welcome to GRK'
# startswith()- Return True if substring starts with the specified prefix, False otherwise
print(str1.startswith('W'))
# True

print(str1.startswith('M'))
# False

# endswith() - Return True if substring ends with the specified suffix, False otherwise
print(str1.endswith('K'))
# True

print(str1.endswith('L'))
# False
