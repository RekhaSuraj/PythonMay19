# isupper() - checks if all the characters in the string is in upper case, returns in bool(True or False)

str1 = 'Mangoes Are Sweet'

print(str1.isupper())
# False
a = str1.upper()
print(a)

print(a.isupper())
# MANGOES ARE SWEET
# True


# islower() - checks if all the characters in the string are in lower case. returns in bool(True or False)
print(str1.islower())
# False

# istitle() - Return True if the string is a title-cased string, False otherwise.
print(str1.istitle())
# True



