# string methods

str1 = 'Python Developer'
# upper() - Return a copy of the string converted to uppercase(capital letter).
res = str1.upper()
print(res)
# PYTHON DEVELOPER

# lower()- Return a copy of the string converted to lowercase(small letter).
print(str1.lower())
# python developer

# title() - Return a version of the string where each word is title cased.
# More specifically, words start with uppercased characters and all remaining cased characters have lower case.
str2 = 'hello world'
print(str2.title())
# Hello World