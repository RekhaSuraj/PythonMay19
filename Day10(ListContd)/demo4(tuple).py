# Tuple

# Tuple is a Built-in datatype in python
# It is used to store multiple values with in a single unit to Assign to a variable name
# Tuple denoted by round braces / Paranthesis( )
# Tuples are immutable - Cannot be modified(Changed) once created

t1 = (10,20,30,40,'hi','hello') #heterogeneous
print(type(t1))
# <class 'tuple'>

# Empty tuple
t2 = ()
print(type(t2))
# <class 'tuple'>

# using a constructor
t3 = tuple((10,20,30,40))
print(type(t3))
print(t3)

# <class 'tuple'>
# (10, 20, 30, 40)

