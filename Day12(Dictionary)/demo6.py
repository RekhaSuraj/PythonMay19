# if else condition/statement:

# This checks the condition and the if block of code when the condition is True, and if the condition is False,
# it will execute the else block of code

# syntax:
# if condition:
#     statement 1:
# else:
#     statement 2

x = True
if x == True:
    print('Condition is True')
else:
    print('Condition is False')

print('Outside of first if condition')
# Condition is True
# Outside of first if condition

print()

x = False
if x == True:
    print('Condition is True')
else:
    print('Condition is False')

print('Outside of second if condition')

# Condition is False
# Outside of second if condition