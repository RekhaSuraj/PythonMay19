# Logical operators 
# There are 3 reserved keywords - and, or and not

# and - Returns True only if both conditions are True, even if one condition is False, it returns False
# syntax : x and y
x = True
y = True

print(x and y)
# True

print(True and False)
# False

print(False and True)
# False

print(False and False)
# False

