# 5. Floor Division(//)

# Divides the number and ignores the decimal part(point) of the quotient
f1 = 20
f2 = 10
res = (f1//f2)
print(res)
# 2

n1 = 9
n2 = 2
print(n1//n2)
# 4

# 6. Modulus(%) - It will give us the remainder after division
a = 30
b = 10
print(a%b)
# 0

print(6%4)
# 2

# 7. Exponentiation(**)
e1 = 4
e2 = 3
print(e1**e2) #4 to the power of 3
# 64

print(8**4) # 8 to the power of 4
# 4096

