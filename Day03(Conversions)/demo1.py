# Conversions:

# Conversion from decimal to binary
# Binary has only 0 and 1, It is prefixed with 0b
# bin(<decimalnumber>) - in built method to convert to binary

# Syntax: bin()
var1 = 23
res = bin(var1)
print(res)

# 0b10111

print(bin(30))
# 0b11110

# Convert from binary to decimal
x = 0b101001
print(x)
# 41


print(0b10111)
# 23