# frozenset
# A frozenset in Python is an immutable version of a set.
# Once created, its elements cannot be changed, added, or removed.

fs = frozenset([1, 2, 3, 4, 5])
print(type(fs))
# <class 'frozenset'>

fs1 = frozenset({7,8,9,10,11})
print(type(fs1))
# <class 'frozenset'>

print(fs1)
# frozenset({7, 8, 9, 10, 11})




