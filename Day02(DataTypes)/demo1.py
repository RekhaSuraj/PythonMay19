# DataTypes:
# Number - int, float, complex
# Bool - True, False
# Set
# Dictionary
# Sequence Datatypes - String, List and Tuple

# Number
# int - integer with positive and negative whole numbers. No decimal points

x = 20

# syntax: type(<variable_name>) - Datatype of the variable
print(type(x))
# <class 'int'>

y = -25
print('Type of y:',type(y))
# Type of y: <class 'int'>


# float - It has decimal point numbers
a = 30.5
print('Type of a:',type(a))
# Type of a: <class 'float'>

b = -45.8
print('Type of b:',type(b))
# Type of b: <class 'float'>


