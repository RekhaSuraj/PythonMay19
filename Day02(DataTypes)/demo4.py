# Type casting:

# Type conversion or typecasting - Converting from one datatype to another datatype is called 
# typecasting/conversion

# Implicit type casting - Python avoids data loss by converting lower datatype(int) to higher datatype(float)
# Typecasting which python interpreter automatically performs without any external typecasting
a = 4.5 #float
b = 2 #int

print(a+b)
# 6.5


